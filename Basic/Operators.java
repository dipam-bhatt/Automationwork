package Basic;

public class Operators {
	public static void main(String[] args) {
		int i=5,j=30,k;
		System.out.println(i+j);
		System.out.println(i-j);
		System.out.println(i*j);
		System.out.println(i/j);
		
		System.out.println("module");
		System.out.println("% = "+(i%j));
		k = i+j;
		System.out.println("k = "+k);
		double a,r=4;
		a = Math.PI*r*r;
		System.out.println("area of circle = "+a);
	}
}
